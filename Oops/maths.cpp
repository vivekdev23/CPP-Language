#include<iostream>
#include<math.h>
#include<stdlib.h>

using namespace std;
int main()
{
    cout<<"value is: "<<floor(5.9)<<endl;
    cout<<"value is: "<<floor(-15.9)<<endl;
    cout<<"value is: "<<ceil(5.2)<<endl;
    cout<<"value is: "<<ceil(-15.6)<<endl;
    cout<<"value is: "<<round(5.2)<<endl;
    cout<<"value is: "<<round(-15.6)<<endl;
    cout<<"value is: "<<sqrt(16)<<endl;
    cout<<"value is: "<<sqrt(5)<<endl;
    cout<<"value is: "<<pow(2,8)<<endl;
    cout<<"value is: "<<pow(5,3)<<endl;
    cout<<"value is: "<<abs(16)<<endl;
    cout<<"value is: "<<abs(-5)<<endl;
    cout<<"value is: "<<rand()%5<<endl;
    cout<<"value is: "<<rand()%100<<endl;
    cout<<"value is: "<<rand()%100<<endl;
}